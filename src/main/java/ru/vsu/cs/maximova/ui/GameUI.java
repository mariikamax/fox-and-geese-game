package ru.vsu.cs.maximova.ui;

import ru.vsu.cs.maximova.board.Position;

/**
 * Интерфейс пользовательского интерфейса для игры.
 * Определяет методы для взаимодействия с игровым процессом.
 */

public interface GameUI {
    /**
     * Отображает пользовательский интерфейс игры.
     * Инициализирует и показывает все необходимые компоненты.
     */
    void display();
    /**
     * Обновляет отображение игровой доски.
     * Вызывается после каждого хода для отражения изменений.
     */
    void updateBoard();
    /**
     * Показывает сообщение пользователю.
     *
     * @param message текст сообщения для отображения
     */
    void showMessage(String message);
    /**
     * Подсвечивает выполненный ход на доске.
     *
     * @param from начальная позиция хода
     * @param to конечная позиция хода
     */
    void highlightMove(Position from, Position to);
}